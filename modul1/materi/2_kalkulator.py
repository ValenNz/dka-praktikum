# Operasi aritmatika dasar
print("=== OPERASI ARITMATIKA DASAR ===")

# Penjumlahan
hasil_tambah = 2 + 2
print(f"2 + 2 = {hasil_tambah}")

# Perkalian
hasil_kali = 5 * 6
print(f"5 * 6 = {hasil_kali}")

# Pengurangan
hasil_kurang = 10 - 5
print(f"10 - 5 = {hasil_kurang}")

# Operasi dengan tanda kurung
hasil_kurung = (3 + 2) * 5
print(f"(3 + 2) * 5 = {hasil_kurung}")

# Prioritas operasi
hasil_prioritas = 5 - 3 * 2
print(f"5 - 3 * 2 = {hasil_prioritas}")

print("\n=== OPERASI PEMBAGIAN ===")

# Pembagian biasa (hasil float)
hasil_bag1 = 10 / 2
print(f"10 / 2 = {hasil_bag1} (tipe: {type(hasil_bag1)})")

# Pembagian integer (hasil bulat)
hasil_bag2 = 10 // 2
print(f"10 // 2 = {hasil_bag2} (tipe: {type(hasil_bag2)})")

# Pembagian dengan sisa
hasil_bag3 = 15 / 4
print(f"15 / 4 = {hasil_bag3}")

hasil_bag4 = 15 // 4
print(f"15 // 4 = {hasil_bag4}")

# Modulus (sisa bagi)
hasil_mod = 15 % 4
print(f"15 % 4 = {hasil_mod}")

# Pangkat
hasil_pangkat = 2 ** 3
print(f"2 ** 3 = {hasil_pangkat}")